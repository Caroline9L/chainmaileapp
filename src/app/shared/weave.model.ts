export class Weave {
 public name: string;
 public imagePath: string;
 public url: string;
 public ringGauge: string;
 public ringSize: string;


 constructor(name: string, imagePath: string, url: string, ringGauge: string, ringSize: string) {
   this.name = name;
   this.imagePath = imagePath;
   this.url = url;
   this.ringGauge = ringGauge;
   this.ringSize = ringSize;
 }
} 

